#include "minishell.h"

void	print_tab(char **tableau)
{
	int	i;

	i = 0;
	printf("------------------------\n");
	while (tableau[i])
	{
		printf("%s*\n", tableau[i++]);
	}
	printf("------------------------\n");
}
